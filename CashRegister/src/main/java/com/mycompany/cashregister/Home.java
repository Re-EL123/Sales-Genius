    private void cartValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_cartValueChanged
        // TODO add your handling code here:

        if (!evt.getValueIsAdjusting()) {
            String selected = cart.getSelectedValue();

            if (selected == null) {
                return;
            }

            switch (selected) {
                case "Apple":

                pricesModel.removeElementAt(cartModel.indexOf(selected));

                cartModel.removeElementAt(cartModel.indexOf(selected));

                cost.setText(String.valueOf(Integer.parseInt(cost.getText()) - 3));

                break;
                case "Banana":

                pricesModel.removeElementAt(cartModel.indexOf(selected));
                cartModel.removeElement(selected);

                cost.setText(String.valueOf(Integer.parseInt(cost.getText()) - 4));

                break;
                case "Orange":

                pricesModel.removeElementAt(cartModel.indexOf(selected));
                cartModel.removeElement(selected);

                cost.setText(String.valueOf(Integer.parseInt(cost.getText()) - 5));

                break;
                case "Milk":

                pricesModel.removeElementAt(cartModel.indexOf(selected));
                cartModel.removeElement(selected);

                cost.setText(String.valueOf(Integer.parseInt(cost.getText()) - 18));

                break;
                case "Bread":

                pricesModel.removeElementAt(cartModel.indexOf(selected));
                cartModel.removeElement(selected);

                cost.setText(String.valueOf(Integer.parseInt(cost.getText()) - 22));

                break;
            }

            outstandingCal.setText(cost.getText());
            itemsCount.setText(cartModel.getSize() + " Items");

        }

    }//GEN-LAST:event_cartValueChanged

    private void productsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_productsListValueChanged
        // TODO add your handling code here:

        if (!evt.getValueIsAdjusting()) {
            outstandingCal.setText(String.valueOf(Integer.parseInt(outstandingCal.getText()) - Integer.parseInt(amountCal.getText())));
            String selected = productsList.getSelectedValue();
            if (selected != null) {
                cartModel.addElement(selected);
                if (selected.equals("Apple")) {
                    cost.setText(String.valueOf(Integer.parseInt(cost.getText()) + 3));
                    pricesModel.addElement("R 3,00");

                }
                if (selected.equals("Banana")) {
                    cost.setText(String.valueOf(Integer.parseInt(cost.getText()) + 4));
                    pricesModel.addElement("R 4,00");

                }
                if (selected.equals("Orange")) {
                    cost.setText(String.valueOf(Integer.parseInt(cost.getText()) + 5));
                    pricesModel.addElement("R 5,00");

                }
                if (selected.equals("Milk")) {
                    cost.setText(String.valueOf(Integer.parseInt(cost.getText()) + 18));
                    pricesModel.addElement("R 18,00");

                }
                if (selected.equals("Bread")) {
                    cost.setText(String.valueOf(Integer.parseInt(cost.getText()) + 22));
                    pricesModel.addElement("R 22,00");

                }
                outstandingCal.setText(cost.getText());
                productsList.clearSelection();
                itemsCount.setText(cartModel.getSize() + " Items");

            }
        }

    }//GEN-LAST:event_productsListValueChanged

