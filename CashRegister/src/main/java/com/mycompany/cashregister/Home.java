    public void exportFrameToPNG(JInternalFrame frame, String filename) {
        try {
            frame.setVisible(true);
            frame.revalidate();
            frame.repaint();

            int w = frame.getWidth();
            int h = frame.getHeight();

            if (w <= 0 || h <= 0) {
                JOptionPane.showMessageDialog(null, "Frame size is invalid. Cannot export.");
                return;
            }

            BufferedImage image = new BufferedImage(w, h, BufferedImage.TYPE_INT_RGB);
            Graphics2D g2 = image.createGraphics();
            frame.paint(g2);
            g2.dispose();

            ImageIO.write(image, "png", new File(filename));
            JOptionPane.showMessageDialog(null, "PNG saved to: " + filename);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Failed to save PNG.");
        }
    }

